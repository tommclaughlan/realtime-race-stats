package com.pushtechnology.diffusion.demos;

public class DoubleRange {
    private final double min;
    private final double max;

    DoubleRange(double min, double max) {
        this.min = min;
        this.max = max;
    }

    double getMin() {
        return min;
    }

    double getMax() {
        return max;
    }
}
